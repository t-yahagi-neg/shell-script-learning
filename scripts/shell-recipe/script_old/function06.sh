#!/bin/bash

# ========================================
# 削除してはいけないファイル・ディレクトリを削除したら警告を出す
# ========================================

# ----------------------------------------
# 変数設定
# ----------------------------------------

exclusion_file=./work/exclusion-file

# ----------------------------------------
# 削除対象を変数へ格納
# ----------------------------------------

target=$1

# ----------------------------------------
# 削除対象を変数へ格納
# ----------------------------------------

grep $target $exclution_file
if [ $? = 0 ]; then
	echo 'これは消せない'
else
	rm $1
fi
