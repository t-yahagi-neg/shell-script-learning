#!/bin/bash

# ========================================
# 重複の排除 
# ========================================

# ----------------------------------------
# 基本検索形態
# ----------------------------------------
echo "重複排除"
target=$1
cat $1 | sort | uniq

# ----------------------------------------
# 大文字と小文字の区別なし
# ----------------------------------------
echo "重複排除 (大文字小文字区別なし)"
target=$1
cat $1 | sort -f | uniq -i

exit
