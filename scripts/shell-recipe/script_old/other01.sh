#!/bin/bash

# ========================================
# ロト6の番号を自動で予想する
# ========================================

# ----------------------------------------
# 変数設定
# ----------------------------------------

min_num=1
max_num=43
limit_num=6

# ----------------------------------------
# 最小値 (min_num) から最大値 (max_num) まで
# 指定工数 (limit_num) を取り出す
# ----------------------------------------

shuf -i$min_num-$max_num -n $limit_num | sort -n
