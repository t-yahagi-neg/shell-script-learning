#!/bin/bash

# ========================================
# 対象検索① 
# ========================================

# ----------------------------------------
# 基本検索形態
# ----------------------------------------
echo "「.sh」で終わるファイルは"
find ./ -name "*.sh"

# ----------------------------------------
# アクセス時間をベースに検索 (最終アクセスがn日前) 
# ----------------------------------------
echo "7日以上前にアクセスされたファイルは"
find ./ -atime +7

# ----------------------------------------
# 修正時間をベースに検索 (最終更新がn日前) 
# ----------------------------------------
echo "7日以上前に更新されたファイルは"
find ./ -mtime +7

# ----------------------------------------
# 作成時間をベースに検索 (作成日がn日前)
# ----------------------------------------
echo "7日以上前に作成されたファイルは"
find ./ -ctime +7
