#!/bin/bash

# ========================================
# ファイル名に日本語や全角数字が混入しているファイルを探し出す
# ========================================

# ----------------------------------------
# 変数設定
# ----------------------------------------

target_file=index.log
target_file=result.log

# ----------------------------------------
# 検索用Indexの作成
# ----------------------------------------

find ./ -type f > $target_file

# ----------------------------------------
# ASCIIにだけ含まれる文字を含んだ検索結果を抜き出し
# ----------------------------------------

LANG=C
grep -v '^[[:cntrl:][:print:]]*$' $target_file > $result_file

# ----------------------------------------
# 結果表示
# ----------------------------------------

cat $result_file

# ----------------------------------------
# 後付け 
# ----------------------------------------

rm $target_file
rm $result_file
