#!/bin/bash

# ========================================
# ディスク容量が一定以下になったら警告を出す
# ========================================

# ----------------------------------------
# 変数設定
# ----------------------------------------

limit_para=65
win_dir=/mnt/c

# ----------------------------------------
# 現在の使用率を算出
# ----------------------------------------

target=`df /mnt/c | tail -1 | sed 's/^.* \([0-9]*\)%.*$/\1/'`

# ----------------------------------------
# 閾値を超えていないか確認
# ----------------------------------------

if [ $target -ge $limit_para ]; then
	echo 'over'
else
	echo 'no over'
fi
