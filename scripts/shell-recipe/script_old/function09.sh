#!/bin/bash

# ========================================
# 使われていない古いディレクトリやファイルを見つける
# ========================================

# ----------------------------------------
# 変数設定
# ----------------------------------------

limit_para=7
target_dir=./
target_file=index.log

# ----------------------------------------
# 最終アクセス時間をベースに検索
# ----------------------------------------

find $target_dir -atime $limit_para -type f > $target_file

# ----------------------------------------
# 結果表示
# ----------------------------------------

cat $target_file

# ----------------------------------------
# 後片付け
# ----------------------------------------

rm $target_file
